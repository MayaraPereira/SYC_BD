    public void acessarSistema(){
        //DataSource dataSource = new DataSource();
        
        try {
            String SQL = "Select * FROM usersyc";
            PreparedStatement ps = getDataSource().getConnection().prepareStatement(SQL);
            ResultSet rs = ps.executeQuery();
            String valor = new String(senhaPasswordField.getPassword());
            TelaMenuPrincipal form = new TelaMenuPrincipal();
            
            while (rs.next()){
                if (emailTextField.getText().equalsIgnoreCase(rs.getString("email")) && valor.equals(rs.getString("senha"))){
                    id_user = rs.getInt("id");
                    form.setNomeUser(rs.getString("nome"));
                    id_user = rs.getInt("id");
                    System.out.println(id_user);
                    dispose();
                    //abra a tela do user
                    form.setVisible(true);
                    break;
                }
            }
            if (rs.next() == false && id_user == -1){
                JOptionPane.showMessageDialog(null, "Dados Inv√°lidos");
            }
            ps.close();
        } catch (SQLException ex) {
            System.err.println("ERRO AO RECUPERAR " + ex.getMessage());
        } catch(Exception ex){
            System.err.println("ERRO GERAL..." + ex.getMessage());
        }
        
    }

